package java_start;

public class SwitchDemo {

	public static void main(String[] args) {
	
		switch(1) {
		
		case 1: System.out.println("one"); 
		break;
		case 2: System.out.println("two");
		break;
		case 3: System.out.println("three");
		break;
	//	default: System.out.println(); 위 해당없을때 실행
		
		}

	}

}
